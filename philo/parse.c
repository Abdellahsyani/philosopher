#include "philo.h"

int	main(int ac, char **av)
{
	int	i;
	int	j;

	if (ac > 6)
		return (1);
	i = 0;
	while (av[i])
	{
		j = 0;
		while (av[i][j])
		{
			if (!(av[i][j] >= '0' && av[i][j] <= '9'))
				return (1);
			j++;
		}
		i++;
	}
	return (0);
}
